import numpy as np
import pandas as pd
from sklearn.model_selection import train_test_split
from sklearn.preprocessing import LabelEncoder, StandardScaler
from sklearn.linear_model import LogisticRegression
from sklearn.metrics import accuracy_score, confusion_matrix, classification_report

data=pd.read_csv('/content/WA_Fn-UseC_-Telco-Customer-Churn.csv') # read the dataset

data.columns

data.head()

data = data.drop('customerID', axis=1)

data.shape # shape of the data

data.isna().sum() # check how many null values are there in the dataset

data.columns # column names

# convert all the categorical values into numerical values
encoders={}
for col in data.columns:
  if data[col].dtype== object:
    le=LabelEncoder()
    data[col]=le.fit_transform(data[col])
    encoders[col]=le

data.head()

# dividing the data
x=data.drop('Churn',axis=1)
y=data['Churn']

# train-test split
x_train,x_test,y_train,y_test=train_test_split(x,y,test_size=0.2,random_state=12345)

x_train.shape,x_test.shape,y_train.shape,y_test.shape

ss=StandardScaler() # to bring all the values of the column into a same range

x_train_ss=ss.fit_transform(x_train)
x_test_ss=ss.transform(x_test)

model=LogisticRegression()

model.fit(x_train_ss,y_train)

result=model.predict(x_test_ss)

accuracy_score(y_test,result) #accuracy score

print(confusion_matrix(y_test, result)) # tells us how well the prdiction is

#how IMP each columns is
importance = pd.DataFrame({
    'Feature': x.columns,
    'Coefficient': model.coef_[0]
})

print(importance.sort_values(by='Coefficient'))

print(classification_report(y_test,result))

import joblib
joblib.dump(model,'model.pkl')
joblib.dump(ss,'scalar.pkl')
joblib.dump(encoders,'encoders.pkl')

